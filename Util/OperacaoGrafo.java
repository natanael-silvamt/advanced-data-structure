package br.ufc.quixada.eda.util;

public class OperacaoGrafo {
	private int aresta;
	private int vertice;
	private int peso;
	
	
	public int getAresta() {
		return aresta;
	}
	public void setAresta(int aresta) {
		this.aresta = aresta;
	}
	public int getVertice() {
		return vertice;
	}
	public void setVertice(int vertice) {
		this.vertice = vertice;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}

}
